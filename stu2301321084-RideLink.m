### 4. Основни бизнес правила

#### 4.1. Правила за достъп и роли

*   **Гост (нерегистриран):** Може само да вижда началната страница и информация за услугата.
*   **Потребител (регистриран):** Може да заявява пътувания, да вижда цена и история.
*   **Шофьор:** Може да приема и изпълнява пътувания.
*   **Администратор:** Има пълен контрол върху системата чрез уеб интерфейс.
  
#### 4.2. Ключови бизнес правила

*   **Пътувания:**
    *   Не могат да съществуват две активни пътувания за един и същи шофьор.
    *   Пътуване може да се стартира само ако и двете страни са го потвърдили.
    *   Цената се изчислява по формулата:
BasePrice + Distance(km) * RatePerKm + Duration(min) * RatePerMin.

*   **Плащания:**
    *   При завършено пътуване системата маркира статуса “Completed”.
    *   Отказ от пътуване по-късно от 5 минути след потвърждение води до такса “Late cancel fee”.
      
*   **Рейтинги и обратна връзка::**
    *   След пътуване пътникът може да оцени шофьора от 1 до 5 звезди.
    *   Средната оценка влияе на приоритета при бъдещи заявки.
    
#### 4.3. Правила за данните

*   **Потребители:** Имейл адресът е уникален за всеки потребител; Ролята (Пътник/Шофьор) се задава при регистрация.
*   **Пътувания:** Всяко пътуване има уникален идентификатор и свързани User_ID и Driver_ID; Статусът може да бъде само един от: Requested, Accepted, InProgress, Completed, Cancelled.
*   **Плащания:** Сумата се изчислява от backend-а и се връща към клиента като JSON отговор; Всички плащания се записват с дата, сума и статус.

