### 2. Обхват и основни функционалности
*Списък с ключовите възможности, които системата ще предоставя.*

*   **Управление на потребители:**
    *   Регистрация и вход (пътник / шофьор).
    *   Редакция на профил и автомобилни данни.
    *   Вход чрез имейл или телефонен номер.
*   **Система за пътувания::**
    *   Пътникът въвежда начална и крайна дестинация.
    *   Приложението изчислява цена спрямо разстоянието и времето.
    *   Заявката се изпраща към сървъра, който намира наличен шофьор.
    *   Шофьорът потвърждава и започва пътуването.
*   **Проследяване и статуси:**
    *  Показване на състоянието: Търси шофьор → Приет курс → В движение → Завършен.
    *  Запис в история на извършените курсове.
*   **Плащания:**
    *   Изчисляване на цена (фиктивно, без реална интеграция).
    *   Показване на сума и метод на плащане (“В брой” / “Карта”).
*   **Административен панел (уеб):**
    *   Преглед на потребители, курсове и плащания.
    *   Ръчно управление на шофьори и проверки на рейтинги.
