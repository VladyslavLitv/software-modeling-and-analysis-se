# Описание на проект: [RideLink (Uber-like App)]

*   **Версия:** 1.0
*   **Дата:** 2025-05-11

---

### 1. Резюме на проекта

| Цел на проекта | Целева аудитория | Основни резултати |

| :Да се разработи мобилно приложение, което свързва шофьори и пътници за споделени пътувания в реално време. Проектът решава проблема с бързия достъп до транспорт и спестява време при придвижване в градска среда. | 
| :Хора, които нямат собствен автомобил, искат да пътуват евтино или да споделят разходите; шофьори, които искат да печелят пари, като превозват пътници. |
| :Мобилно Android приложение (на Kotlin) и RESTful уеб услуга (на ASP.NET Core), които обменят данни чрез API. |

